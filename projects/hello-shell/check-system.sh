#!/bin/bash
# 系统状态检查脚本 - 第1周项目
# 功能：输出当前系统的关键状态信息

echo "========================================"
echo "  系统状态巡检报告"
echo "  日期: $(date '+%Y-%m-%d %H:%M:%S')"
echo "========================================"

# 1. 系统信息
echo ""
echo "[1/5] 系统信息"
echo "  主机名: $(hostname)"
echo "  系统:   $(uname -s) $(uname -r)"

# 2. CPU 使用率（top 命令快照）
echo ""
echo "[2/5] CPU 负载"
echo "  $(uptime)"

# 3. 内存使用
echo ""
echo "[3/5] 内存使用"
free -h | awk 'NR==2 {printf "  总计: %s | 已用: %s | 可用: %s | 使用率: %.1f%%\n", $2, $3, $4, $3/$2*100}'

# 4. 磁盘使用
echo ""
echo "[4/5] 磁盘使用"
df -h / | awk 'NR==2 {printf "  总计: %s | 已用: %s | 可用: %s | 使用率: %s\n", $2, $3, $4, $5}'

# 5. 进程数
echo ""
echo "[5/5] 进程统计"
echo "  当前运行进程数: $(ps aux | wc -l)"

echo ""
echo "========================================"
echo "  巡检完成"
echo "========================================"
